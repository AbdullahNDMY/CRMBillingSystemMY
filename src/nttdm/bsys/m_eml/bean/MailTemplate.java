package nttdm.bsys.m_eml.bean;

public class MailTemplate{
	
	private String row_num;
	private String code;
	private String description;
	private String subject;
	private String alwaysCc;
	/**
	 * @return the row_num
	 */
	public String getRow_num() {
		return row_num;
	}
	/**
	 * @param row_num the row_num to set
	 */
	public void setRow_num(String row_num) {
		this.row_num = row_num;
	}
	/**
	 * @return the code
	 */
	public String getCode() {
		return code;
	}
	/**
	 * @param code the code to set
	 */
	public void setCode(String code) {
		this.code = code;
	}
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	/**
	 * @return the subject
	 */
	public String getSubject() {
		return subject;
	}
	/**
	 * @param subject the subject to set
	 */
	public void setSubject(String subject) {
		this.subject = subject;
	}
	/**
	 * @return the alwaysCc
	 */
	public String getAlwaysCc() {
		return alwaysCc;
	}
	/**
	 * @param alwaysCc the alwaysCc to set
	 */
	public void setAlwaysCc(String alwaysCc) {
		this.alwaysCc = alwaysCc;
	}

}
