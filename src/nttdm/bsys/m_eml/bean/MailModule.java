package nttdm.bsys.m_eml.bean;


public class MailModule {

	private String row_num;
	private String module;
	private String subModule;
	private String temCode;
	private Integer id_audit;
	private String id_login;
	/**
	 * @return the row_num
	 */
	public String getRow_num() {
		return row_num;
	}
	/**
	 * @param row_num the row_num to set
	 */
	public void setRow_num(String row_num) {
		this.row_num = row_num;
	}
	/**
	 * @return the module
	 */
	public String getModule() {
		return module;
	}
	/**
	 * @param module the module to set
	 */
	public void setModule(String module) {
		this.module = module;
	}
	/**
	 * @return the subModule
	 */
	public String getSubModule() {
		return subModule;
	}
	/**
	 * @param subModule the subModule to set
	 */
	public void setSubModule(String subModule) {
		this.subModule = subModule;
	}
	/**
	 * @return the temCode
	 */
	public String getTemCode() {
		return temCode;
	}
	/**
	 * @param temCode the temCode to set
	 */
	public void setTemCode(String temCode) {
		this.temCode = temCode;
	}
	/**
	 * @return the id_audit
	 */
	public Integer getId_audit() {
		return id_audit;
	}
	/**
	 * @param id_audit the id_audit to set
	 */
	public void setId_audit(Integer id_audit) {
		this.id_audit = id_audit;
	}
	/**
	 * @return the id_login
	 */
	public String getId_login() {
		return id_login;
	}
	/**
	 * @param id_login the id_login to set
	 */
	public void setId_login(String id_login) {
		this.id_login = id_login;
	}

}
