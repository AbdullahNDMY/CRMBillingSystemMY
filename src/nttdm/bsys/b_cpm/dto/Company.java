package nttdm.bsys.b_cpm.dto;

public class Company {
	private String idCompany;
	private String companyName;
	private String companyTelephoneNo;
	private String companyFaxNo;
	private String companyWebsite;
	private String affiliateCode;
	private String proxyServerName;
	private String primaryDomainNo;
	private String secondaryDomainNo;
	private String companyRegistration;
	private String portNo;
	public String getIdCompany() {
		return idCompany;
	}
	public void setIdCompany(String idCompany) {
		this.idCompany = idCompany;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getCompanyTelephoneNo() {
		return companyTelephoneNo;
	}
	public void setCompanyTelephoneNo(String companyTelephoneNo) {
		this.companyTelephoneNo = companyTelephoneNo;
	}
	public String getCompanyFaxNo() {
		return companyFaxNo;
	}
	public void setCompanyFaxNo(String companyFaxNo) {
		this.companyFaxNo = companyFaxNo;
	}
	public String getCompanyWebsite() {
		return companyWebsite;
	}
	public void setCompanyWebsite(String companyWebsite) {
		this.companyWebsite = companyWebsite;
	}
	public String getAffiliateCode() {
		return affiliateCode;
	}
	public void setAffiliateCode(String affiliateCode) {
		this.affiliateCode = affiliateCode;
	}
	public String getProxyServerName() {
		return proxyServerName;
	}
	public void setProxyServerName(String proxyServerName) {
		this.proxyServerName = proxyServerName;
	}
	public String getPrimaryDomainNo() {
		return primaryDomainNo;
	}
	public void setPrimaryDomainNo(String primaryDomainNo) {
		this.primaryDomainNo = primaryDomainNo;
	}
	public String getSecondaryDomainNo() {
		return secondaryDomainNo;
	}
	public void setSecondaryDomainNo(String secondaryDomainNo) {
		this.secondaryDomainNo = secondaryDomainNo;
	}
	public String getCompanyRegistration() {
		return companyRegistration;
	}
	public void setCompanyRegistration(String companyRegistration) {
		this.companyRegistration = companyRegistration;
	}
	public String getPortNo() {
		return portNo;
	}
	public void setPortNo(String portNo) {
		this.portNo = portNo;
	}
	
}
